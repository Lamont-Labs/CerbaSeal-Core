// ─── Step 4: Approvers ────────────────────────────────────────────────────────

function Step4({ state, update }: { state: WizardState; update: (p: Partial<WizardState>) => void }) {
  function updateApprover(i: number, patch: Partial<Approver>) {
    const next = state.approvers.map((a, idx) => (idx === i ? { ...a, ...patch } : a));
    update({ approvers: next });
  }

  function addApprover() {
    update({ approvers: [...state.approvers, { clientRoleName: "", authorityClass: "" }] });
  }

  function removeApprover(i: number) {
    if (state.approvers.length > 1) {
      update({ approvers: state.approvers.filter((_, idx) => idx !== i) });
    }
  }

  return (
    <div className="space-y-5">
      <div className="bg-slate-800/50 border border-slate-700 rounded-lg p-4 text-sm text-slate-300">
        Who in your organization has the authority to approve AI-proposed actions? Map each role name to the closest CerbaSeal authority class.
      </div>

      {state.approvers.map((approver, i) => (
        <div key={i} className="bg-slate-900 border border-slate-700 rounded-xl p-5">
          <div className="flex items-center justify-between mb-4">
            <span className="text-white font-medium text-sm">Approver role {i + 1}</span>
            {state.approvers.length > 1 && (
              <button
                type="button"
                onClick={() => removeApprover(i)}
                className="text-slate-500 hover:text-red-400 transition-colors"
              >
                <Trash2 className="w-4 h-4" />
              </button>
            )}
          </div>

          <div className="space-y-4">
            <div>
              <FieldLabel>Role name in your organization <span className="text-red-400">*</span></FieldLabel>
              <TextInput
                value={approver.clientRoleName}
                onChange={(v) => updateApprover(i, { clientRoleName: v })}
                placeholder="e.g. Senior Fraud Analyst"
              />
            </div>

            <div>
              <FieldLabel>CerbaSeal authority class <span className="text-red-400">*</span></FieldLabel>
              <div className="space-y-2">
                {AUTHORITY_CLASSES.map((cls) => (
                  <button
                    key={cls.value}
                    type="button"
                    onClick={() => updateApprover(i, { authorityClass: cls.value })}
                    className={`w-full text-left px-4 py-3 rounded-lg border text-sm transition-all ${
                      approver.authorityClass === cls.value
                        ? "border-blue-500 bg-blue-950"
                        : "border-slate-700 bg-slate-800 hover:border-slate-600"
                    }`}
                  >
                    <div className={`font-medium ${approver.authorityClass === cls.value ? "text-blue-300" : "text-white"}`}>
                      <code className="text-xs mr-2">{cls.value}</code>{cls.label}
                    </div>
                    <div className="text-xs text-slate-400 mt-0.5">{cls.desc}</div>
                  </button>
                ))}
              </div>
            </div>
          </div>
        </div>
      ))}

      <button
        type="button"
        onClick={addApprover}
        className="w-full border border-dashed border-slate-600 rounded-xl py-3 text-slate-400 hover:text-slate-300 hover:border-slate-500 transition-colors text-sm flex items-center justify-center gap-2"
      >
        <Plus className="w-4 h-4" /> Add another approver role
      </button>
    </div>
  );
}

