// ─── Zip download ─────────────────────────────────────────────────────────────

async function downloadConfigPackage(s: WizardState) {
  const zip = new JSZip();
  zip.file("cerbaseal.policy.json", generatePolicyJson(s));
  zip.file("pilot-checklist.md", generatePilotChecklist(s));
  zip.file("scenario-tests.ts", generateScenarioTests(s));
  zip.file("handoff-pack.md", generateHandoffPack(s));

  const blob = await zip.generateAsync({ type: "blob" });
  const url = URL.createObjectURL(blob);
  const a = document.createElement("a");
  a.href = url;
  const slug = toSnakeCase(s.workflowName) || "cerbaseal";
  a.download = `cerbaseal-config-${slug}.zip`;
  document.body.appendChild(a);
  a.click();
  document.body.removeChild(a);
  URL.revokeObjectURL(url);
}

