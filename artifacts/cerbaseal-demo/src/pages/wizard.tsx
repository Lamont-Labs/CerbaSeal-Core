// ─── Validation ───────────────────────────────────────────────────────────────

function validateStep(step: number, state: WizardState): string | null {
  if (step === 1) {
    if (!state.workflowName.trim()) return "Workflow name is required.";
    if (!state.workflowDescription.trim()) return "Workflow description is required.";
    if (!state.workflowFrequency) return "Please select how often this workflow runs.";
    if (!state.workflowEnvironment) return "Please select the current status.";
    if (!state.workflowSector) return "Please select the industry or sector.";
  }
  if (step === 2) {
    if (!state.actions[0]?.name.trim()) return "At least one action with a name is required.";
    for (let i = 0; i < state.actions.length; i++) {
      if (state.actions[i].name && !state.actions[i].reversible) {
        return `Please specify if action ${i + 1} is reversible.`;
      }
    }
  }
  if (step === 3) {
    if (!state.aiOutput.trim()) return "Please describe what your AI system produces.";
    if (!state.aiHosting) return "Please select where the AI model is hosted.";
    if (!state.aiSelfApproval) return "Please answer the self-approval question.";
  }
  if (step === 4) {
    if (!state.approvers[0]?.clientRoleName.trim()) return "At least one approver role name is required.";
    if (!state.approvers[0]?.authorityClass) return "Please select a CerbaSeal authority class for the first approver.";
    for (let i = 0; i < state.approvers.length; i++) {
      if (state.approvers[i].clientRoleName && !state.approvers[i].authorityClass) {
        return `Please select an authority class for approver ${i + 1}.`;
      }
    }
  }
  if (step === 5) {
    if (!state.deploymentMode) return "Please select a deployment mode.";
  }
  return null;
}

// ─── Wizard shell ─────────────────────────────────────────────────────────────

const STEP_LABELS = [
  "Workflow",
  "Actions",
  "AI Role",
  "Approvers",
  "Deployment",
  "Summary",
];

export default function Wizard() {
  const [step, setStep] = useState(1);
  const [state, setState] = useState<WizardState>(INITIAL_STATE);
  const [error, setError] = useState<string | null>(null);

  function update(patch: Partial<WizardState>) {
    setState((s) => ({ ...s, ...patch }));
    setError(null);
  }

  function next() {
    const err = validateStep(step, state);
    if (err) {
      setError(err);
      return;
    }
    setError(null);
    setStep((s) => Math.min(6, s + 1));
    window.scrollTo({ top: 0, behavior: "smooth" });
  }

  function back() {
    setError(null);
    setStep((s) => Math.max(1, s - 1));
    window.scrollTo({ top: 0, behavior: "smooth" });
  }

  const progress = ((step - 1) / 5) * 100;

  return (
    <main className="max-w-2xl mx-auto px-6 py-12">
      {/* Header */}
      <div className="mb-8">
        <div className="flex items-center gap-3 mb-2">
          <Wand2 className="w-6 h-6 text-violet-400" />
          <h1 className="text-2xl font-bold text-white">Workflow Mapping Wizard</h1>
        </div>
        <p className="text-slate-400 text-sm">
          6 steps · 30–60 minutes · generates a complete configuration package
        </p>
      </div>

      {/* Progress */}
      <div className="mb-8">
        <div className="flex items-center justify-between mb-2">
          <span className="text-slate-400 text-xs">
            Step {step} of 6 — {STEP_LABELS[step - 1]}
          </span>
          <span className="text-slate-500 text-xs">{Math.round(progress)}% complete</span>
        </div>
        <div className="w-full bg-slate-800 rounded-full h-1.5">
          <div
            className="bg-violet-500 h-1.5 rounded-full transition-all duration-300"
            style={{ width: `${progress}%` }}
          />
        </div>

        {/* Step dots */}
        <div className="flex gap-1 mt-3 justify-between">
          {STEP_LABELS.map((label, i) => {
            const n = i + 1;
            return (
              <button
                key={label}
                type="button"
                onClick={() => {
                  if (n < step) { setStep(n); setError(null); }
                }}
                disabled={n >= step}
                className={`flex flex-col items-center gap-1 group disabled:cursor-default`}
              >
                <div className={`w-2 h-2 rounded-full transition-colors ${
                  n < step ? "bg-violet-400" : n === step ? "bg-violet-500" : "bg-slate-700"
                }`} />
                <span className={`text-xs hidden sm:block ${
                  n === step ? "text-violet-400" : n < step ? "text-slate-400 group-hover:text-slate-300" : "text-slate-600"
                }`}>{label}</span>
              </button>
            );
          })}
        </div>
      </div>

      {/* Step content */}
      <div className="mb-6">
        {step === 1 && <Step1 state={state} update={update} />}
        {step === 2 && <Step2 state={state} update={update} />}
        {step === 3 && <Step3 state={state} update={update} />}
        {step === 4 && <Step4 state={state} update={update} />}
        {step === 5 && <Step5 state={state} update={update} />}
        {step === 6 && <Step6 state={state} />}
      </div>

      {/* Error */}
      {error && (
        <div className="mb-4 bg-red-950 border border-red-700 rounded-lg px-4 py-3 text-red-300 text-sm flex items-center gap-2">
          <AlertTriangle className="w-4 h-4 flex-shrink-0" />
          {error}
        </div>
      )}

      {/* Navigation */}
      <div className="flex justify-between">
        <Button
          type="button"
          onClick={back}
          disabled={step === 1}
          variant="outline"
          className="border-slate-700 text-slate-300 disabled:opacity-30"
        >
          <ChevronLeft className="w-4 h-4 mr-1" /> Back
        </Button>

        {step < 6 && (
          <Button
            type="button"
            onClick={next}
            className="bg-violet-600 hover:bg-violet-500 text-white"
          >
            Next <ChevronRight className="w-4 h-4 ml-1" />
          </Button>
        )}
      </div>
    </main>
  );
}
