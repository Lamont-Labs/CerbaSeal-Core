function generatePolicyJson(s: WizardState): string {
  const workflowClass = toSnakeCase(s.workflowName) || "unnamed_workflow";

  const actorMappings: Record<string, string> = {};
  for (const ap of s.approvers) {
    if (ap.clientRoleName && ap.authorityClass) {
      actorMappings[ap.clientRoleName] = ap.authorityClass;
    }
  }

  const approverClasses = s.approvers
    .filter((ap) => ap.authorityClass)
    .map((ap) => ap.authorityClass);

  const actionPolicies: Record<string, Record<string, string>> = {};
  const workflowActions: Record<string, string> = {};
  for (const action of s.actions) {
    if (action.name) {
      const ac = toSnakeCase(action.name);
      workflowActions[ac] = action.consequential ? "requires_approval" : "auto_allow";
    }
  }
  if (Object.keys(workflowActions).length > 0) {
    actionPolicies[workflowClass] = workflowActions;
  }

  const policy: Record<string, unknown> = {
    _schema: "cerbaseal-policy/v1",
    _generated: new Date().toISOString(),
    _workflowName: s.workflowName,
  };

  if (Object.keys(actorMappings).length > 0) {
    policy["actorMappings"] = actorMappings;
  }

  if (approverClasses.length > 0) {
    policy["approvalChains"] = {
      [workflowClass]: [...new Set(approverClasses)],
    };
  }

  if (Object.keys(actionPolicies).length > 0) {
    policy["actionPolicies"] = actionPolicies;
  }

  return JSON.stringify(policy, null, 2);
}

